# 5. Band Name Generator (A simple project often introduced on Day 1)
#we are generating our own band name
print("Welcome to the Band Name Generator.")
#giving city name as input
city = input("What's the name of the city you grew up in?\n")
#giving pet name as input
pet = input("What's your pet's name?\n")
#combining city name and pet name to create band name
print("Your band name could be " + city + " " + pet)
